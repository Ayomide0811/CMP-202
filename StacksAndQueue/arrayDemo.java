package StacksAndQueue;

public class arrayDemo {
    public static void main(String[] args) {
        int[] numbersArray = new int[10];
        numbersArray[0] = 17;
        numbersArray[1] = 34;
        numbersArray[2] = 56;
        numbersArray[3] = 78;
        numbersArray[4] = 90;

        for (int i = 0; i <= numbersArray.length; i++) {
            System.out.println(numbersArray[i]);

        }

    }
}